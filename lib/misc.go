/* IsTrue() defaults to false if the string is not known */
func IsTrue(val string) (b bool) {
	if val == "yes" || val == "true" || val == "on" {
		b = true
	} else {
		b = false
	}

	return b
}

func YesNo(b bool) (val string) {
	if b == true {
		return "yes"
	}

	return "no"
}

func NormalizeBoolean(val string) string {
	return YesNo(IsTrue(val))
}

