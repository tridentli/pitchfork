func DbgA(off int, format string, a ...interface{}) {
	if !Debug {
		return
	}

	where := Where(1 + off)

	pc := make([]uintptr, 10+off)
	runtime.Callers(2+off, pc)
	f := runtime.FuncForPC(pc[0])

	OutA("Debug("+where+") "+f.Name()+" "+format, a...)
}

